<b>General Dog Care<br><br></b>
<img id="dog_img" src="dog.jpg" alt="dog"><br><br>
A dog can be a wonderful addition to any home, but whether you're an experienced pet parent or a first-time adopter,<br> 
it's important to keep your canine companion's health and happiness a top priority. Below are some useful tips for all dog parents.<br>
And remember: If you're considering bringing home a new dog, please make adoption your first option.<br><br>
<b>Feeding</b><br>
<ul><li>Puppies eight to 12 weeks old need four meals a day.<br></li>
<li>Feed puppies three to six months old three meals a day.<br></li>
<li>Feed puppies six months to one year two meals a day.<br></li>
<li>When your dog reaches his first birthday, one meal a day is usually enough.<br></li>
<li>For some dogs, including larger canines or those prone to bloat, it's better to feed two smaller meals.<br></li></ul>
Premium-quality dry food provides a well-balanced diet for adult dogs and may be mixed with water, broth or canned food.<br> 
Your dog may enjoy cottage cheese, cooked egg or fruits and vegetables, but these additions should not total more than ten percent of his daily food intake.<br><br>

Puppies should be fed a high-quality, brand-name puppy food (large breed puppy foods for large breeds). Please limit "people food," however,<br> 
because it can result in vitamin and mineral imbalances, bone and teeth problems and may cause very picky eating habits and obesity. Clean, fresh water<br>
should be available at all times, and be sure to wash food and water dishes frequently.<br><br>

<b>Exercise</b><br><br>
Dogs need exercise to burn calories, stimulate their minds, and stay healthy. Individual exercise needs vary based on breed or breed mix, sex, age and level of health.<br> 
Exercise also tends to help dogs avoid boredom, which can lead to destructive behaviors. Supervised fun and games will satisfy many of your pet's instinctual urges to dig,<br> 
herd, chew, retrieve and chase.<br><br>

<b>Grooming</b><br><br>
Help keep your dog clean and reduce shedding with frequent brushing. Check for fleas and ticks daily during warm weather. Most dogs don't need to be bathed more than a few<br> 
times a year. Before bathing, comb or cut out all mats from the coat. Carefully rinse all soap out of the coat, or the dirt will stick to soap residue. Please visit our Dog Grooming<br> 
Tips page for more information.<br><br>

<b>Handling</b><br><br>
To carry a puppy or small dog, place one hand under the dog's chest, with either your forearm or other hand supporting the hind legs and rump. Never attempt to lift or grab your puppy<br> 
or small dog by the forelegs, tail or back of the neck. If you do have to lift a large dog, lift from the underside, supporting his chest with one arm and his rear end with the other.<br><br>

<b>Housing</b><br><br>
Your pet needs a warm, quiet place to rest, away from all drafts and off the floor. A training crate or dog bed is ideal, with a clean blanket or pillow placed inside. Wash the dog's bedding<br> 
often. If your dog will be spending a lot of time outdoors, be sure she has access to shade and plenty of cool water in hot weather, and a warm, dry, covered shelter when it's cold.<br><br>

<b>Licensing and Identification</b><br><br>
Follow your community’s licensing regulations. Be sure to attach the license to your dog’s collar. This, along with an ID tag and implanted microchip or tattoo,<br> 
can help secure your dog’s return should she become lost.<br><br>

<b>Fleas and Ticks</b><br><br>
Daily inspections of your dog for fleas and ticks during the warm seasons are important. Use a flea comb to find and remove fleas. There are several new methods of flea and tick control.<br> 
Speak to your veterinarian about these and other options. Visit our Fleas and Ticks page for more information.<br><br>

<b>Medicines and Poisons</b><br><br>
Never give your dog medication that has not been prescribed by a veterinarian. If you suspect that your animal has ingested a poisonous substance, call your veterinarian or the ASPCA Animal<br> 
Poison Control Center for 24-hour animal poison information at (888) 426- 4435.<br><br>

<b>Spaying and Neutering</b><br><br>
Female dogs should be spayed and male dogs neutered by six months of age.<br><br>

<b>Vaccinations</b><br><br>
Your dog may benefit from receiving a number of vaccinations. Please visit our Pet Vaccinations page to learn more.<br><br>

