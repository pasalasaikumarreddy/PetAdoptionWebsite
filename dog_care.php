<b>The Scoop on Poop<br></b><br>
Keep your dog on a leash when you are outside, unless you are in a secured, fenced-in area. If your dog defecates on a neighbor's lawn,<br> 
the sidewalk or any other public place, please clean it up.<br><br><br><br></div>
